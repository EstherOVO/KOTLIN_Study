package f_exercise

/*
연습 문제 1 : 기본 변수 선언과 할당

변수 name과 age를 선언하고, 각각 "철수"와 25라는 값을 할당한 후,
"철수는 25살입니다."라는 문장을 출력하는 코틀린 프로그램을 작성하세요.

요구사항 :
- name은 문자열 타입으로 선언하고 "철수"라는 값을 할당하세요.
- age는 정수 타입으로 선언하고 25라는 값을 할당하세요.
- 출력은 println() 함수를 사용하여 표준 출력으로 표시합니다.
*/

fun main() {

    val name: String = "철수"
    val age: Int = 25

    println("${name}는 ${age}살입니다.")
}